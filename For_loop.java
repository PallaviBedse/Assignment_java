package assigment_module;

public class For_loop {

	public static void main(String[] args) {
		
	System.out.println("print the value of 10 to 40");
		 for(int i=10; i<=40; i++)
		 { 
			 System.out.print("  " +i); 
		  }
	 System.out.println("");
	 System.out.println("print the value of -10 to -40");
		 for (int i=-10; i>=-40; i--)
		 {
			System.out.print(i);
		 }
	 System.out.println("");
	 System.out.println("print the value of -10 to -10");
		 for (int i=-10; i<=10; i++)
		 {
			System.out.print(" " +i);
		 }
	}

}
